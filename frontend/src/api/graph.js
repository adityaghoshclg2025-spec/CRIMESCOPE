import service, { requestWithRetry } from './index'

/**
 * translated text（translated text）
 * @param {Object} data - Includefiles, simulation_requirement, project_namewait
 * @returns {Promise}
 */
export function generateOntology(formData) {
  return requestWithRetry(() => 
    service({
      url: '/api/graph/ontology/generate',
      method: 'post',
      data: formData,
      headers: {
        'Content-Type': 'multipart/form-data'
      }
    })
  )
}

/**
 * translated text
 * @param {Object} data - Includeproject_id, graph_namewait
 * @returns {Promise}
 */
export function buildGraph(data) {
  return requestWithRetry(() =>
    service({
      url: '/api/graph/build',
      method: 'post',
      data
    })
  )
}

